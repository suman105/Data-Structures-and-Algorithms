class Solution{
public:	
	
	
	int isPalindrome(string s)
	{
	    int l,h;
        l=0;h=s.size()-1;
        while(h > l){
            if( s[h] != s[l]){
                return 0;
            }
            ++l;--h;
        }
        return 1;
	}

};

