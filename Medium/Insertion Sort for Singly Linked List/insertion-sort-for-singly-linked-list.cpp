class Solution
{
    public:
    void iSort(int arr[], int n)
{
    int i, key, j;
    for (i = 1; i < n; i++)
    {
        key = arr[i];
        j = i - 1;
        while (j >= 0 && arr[j] > key)
        {
            arr[j + 1] = arr[j];
            j = j - 1;
        }
        arr[j + 1] = key;
    }
}
    public:
    Node* insertionSort(struct Node* head)
    {

        int i,a[100000],c=0;
        struct Node *t,*l,*m;
        t=head;
        while(t!=NULL){
            a[c++]=t->data;
            t=t->next;
        }
        iSort(a,c);
        m = new Node(a[0]);
        t=l=m;
        for(i=1;i<c;i++){
            m = new Node(a[i]);
            t->next = m;
            t=m;
        }
        return l;
    }
    
};

