class Solution
{
  public:
    bool perfect(Node* root, Node* head)
    {
        if(!root && !head)  return true;
        else if((!root && head) || (!head && root)) return false;
        else if(root->data != head->data)   return false;
        return perfect(root->left,head->left) && perfect(root->right,head->right);
    }
  
    void mainfunction(Node *root, Node* head,bool &found)
    {
        if(!root)   return;
        else if(root->data == head->data)
        {
            if(perfect(root,head)==true){
                found=true;
                return;
            }
        }
        mainfunction(root->left,head,found);
        mainfunction(root->right,head,found);
    }
    
    bool isSubTree(Node* T, Node* S) 
    {
        bool found=false;
        mainfunction(T,S,found);
        return found;
    }
};


